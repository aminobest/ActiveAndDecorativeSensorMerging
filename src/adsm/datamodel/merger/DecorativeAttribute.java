package adsm.datamodel.merger;

public class DecorativeAttribute {

	String type ;
	String value ;
	
	public DecorativeAttribute(String type, String value) {
		super();
		this.type = type;
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public String getValue() {
		return value;
	}
	
	
}
